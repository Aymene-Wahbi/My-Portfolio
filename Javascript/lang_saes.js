function changeLanguage(lang) {
    if (lang === "ar") {
        document.documentElement.dir = "rtl";
        document.documentElement.lang = "ar";
    } else {
        document.documentElement.dir = "ltr";
        document.documentElement.lang = lang;
    }
    document.getElementById('MyPortfolio').textContent = translations[lang].MyPortfolio;
    const audio = document.getElementById('ambientAudio');
    const musicStatus = document.getElementById('music-status');
    if (audio && musicStatus) {
        musicStatus.textContent = audio.paused
            ? translations[lang]['music-status-disable']
            : translations[lang]['music-status-enable'];
    }
    const intro = document.querySelector('h4.my-4');
    if (intro) intro.textContent = translations[lang]['intro'];
    // Menu de navigation
    const navLinks = document.querySelectorAll(".nav-link");
    if (navLinks.length >= 4) {
        navLinks[0].childNodes[0].nodeValue = translations[lang]['navAccueil'] + " ";
        navLinks[1].childNodes[0].nodeValue = translations[lang]['navApropos'] + " ";
        navLinks[2].childNodes[0].nodeValue = translations[lang]['navCV'] + " ";
        navLinks[3].childNodes[0].nodeValue = translations[lang]['navSAE'] + " ";
    }
    // Cartes SAE
    const cards = document.querySelectorAll(".card-body");
    if (cards.length >= 4) {
        cards[0].querySelector("h3").textContent = translations[lang]['sae101'];
        cards[0].querySelector("h5").textContent = translations[lang]['sae101-title'];
        cards[0].querySelector("p").innerHTML = translations[lang]['sae101-desc'];
        cards[0].querySelector("a").textContent = translations[lang]['see'];

        cards[1].querySelector("h3").textContent = translations[lang]['sae102'];
        cards[1].querySelector("h5").textContent = translations[lang]['sae102-title'];
        cards[1].querySelector("p").innerHTML = translations[lang]['sae102-desc'];
        cards[1].querySelector("a").textContent = translations[lang]['see'];

        cards[2].querySelector("h3").textContent = translations[lang]['sae104'];
        cards[2].querySelector("h5").textContent = translations[lang]['sae104-title'];
        cards[2].querySelector("p").innerHTML = translations[lang]['sae104-desc'];
        cards[2].querySelector("a").textContent = translations[lang]['see'];

        cards[3].querySelector("h3").textContent = translations[lang]['sae201'];
        cards[3].querySelector("h5").textContent = translations[lang]['sae201-title'];
        cards[3].querySelector("p").innerHTML = translations[lang]['sae201-desc'];
        cards[3].querySelector("a").textContent = translations[lang]['see'];
    }
}

document.addEventListener('DOMContentLoaded', function() {
    const langSelector = document.getElementById('custom-content');
    const audio = document.getElementById('ambientAudio');
    const musicStatus = document.getElementById('music-status');
    changeLanguage(langSelector.value);
    langSelector.addEventListener('change', function() {
        changeLanguage(this.value);
    });
    if (musicStatus && audio) {
        musicStatus.addEventListener('click', function() {
            if (audio.paused) {
                audio.play();
            } else {
                audio.pause();
            }
            musicStatus.textContent = audio.paused
                ? translations[langSelector.value]['music-status-disable']
                : translations[langSelector.value]['music-status-enable'];
        });
        audio.addEventListener('play', function() {
            musicStatus.textContent = translations[langSelector.value]['music-status-enable'];
        });
        audio.addEventListener('pause', function() {
            musicStatus.textContent = translations[langSelector.value]['music-status-disable'];
        });
    }
});
