document.addEventListener("DOMContentLoaded", function() {
    const audio = document.getElementById("ambientAudio");
    const musicStatus = document.getElementById("music-status");

    function updateMusicText() {
        if (audio.paused) {
            musicStatus.textContent = {
                fr: "Activer la musique",
                en: "Enable music",
                ar: "تشغيل الموسيقى",
                es: "Activar música",
                de: "Musik einschalten"
            }[document.documentElement.lang] || "Activer la musique";
        } else {
            musicStatus.textContent = {
                fr: "Désactiver la musique",
                en: "Disable music",
                ar: "إيقاف الموسيقى",
                es: "Desactivar música",
                de: "Musik ausschalten"
            }[document.documentElement.lang] || "Désactiver la musique";
        }
    }

    updateMusicText();

    musicStatus.style.cursor = "pointer";
    musicStatus.addEventListener("click", () => {
        if (audio.paused) {
            audio.play();
        } else {
            audio.pause();
        }
        updateMusicText();
    });

    audio.addEventListener("play", updateMusicText);
    audio.addEventListener("pause", updateMusicText);

    const originalTranslatePage = translatePage;
    translatePage = function(lang) {
        originalTranslatePage(lang);
        updateMusicText();
    };
});