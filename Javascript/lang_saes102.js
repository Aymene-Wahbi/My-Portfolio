function changeLanguage(lang) {
    const t = translations[lang];
    if (!t) return;

    if (lang === "ar") {
        document.documentElement.dir = "rtl";
        document.documentElement.lang = "ar";
    } else {
        document.documentElement.dir = "ltr";
        document.documentElement.lang = lang;
    }

    const navLinks = document.querySelectorAll(".nav-link");
    if (navLinks.length >= 4) {
        navLinks[0].childNodes[0].nodeValue = t.navAccueil + " ";
        navLinks[1].childNodes[0].nodeValue = t.navApropos + " ";
        navLinks[2].childNodes[0].nodeValue = t.navCV + " ";
        navLinks[3].childNodes[0].nodeValue = t.navSAE + " ";
    }

    const myPortfolio = document.getElementById("MyPortfolio");
    if (myPortfolio) myPortfolio.textContent = t.MyPortfolio;

    const headerSection = document.querySelector("header > div.my-4");
    if (headerSection) {
        const h2 = headerSection.querySelector("h2");
        if (h2) h2.textContent = t["sae-title"];

        const p1 = headerSection.querySelector(".Experience p.px-3");
        if (p1) p1.innerHTML = t["sae-description"];
    }

    const learnedTitle = document.getElementById("learned-title");
    if (learnedTitle) learnedTitle.textContent = t["learned-title"];

    const learnedList = document.querySelector(".Experience ul.px-4");
    if (learnedList) {
        learnedList.innerHTML = `
      <li>${t.learned1}</li>
      <li>${t.learned2}</li>
      <li>${t.learned3}</li>
      <li>${t.learned4}</li>
    `;
    }

    const desc2 = document.getElementById("sae-description2");
    if (desc2) desc2.innerHTML = t["sae-description2"];

    const desc3 = document.getElementById("sae-description3");
    if (desc3) desc3.innerHTML = t["sae-description3"];

    const resultTitle = document.getElementById("result-title");
    if (resultTitle) resultTitle.textContent = t["result-title"];

    const prevBtn = document.querySelector(".resume-download a.btn-outline-primary:first-child");
    const nextBtn = document.querySelector(".resume-download a.btn-outline-primary:nth-child(2)");
    const backTopBtn = document.getElementById("back_top");

    if (prevBtn) prevBtn.textContent = t.prev;
    if (nextBtn) nextBtn.textContent = t.next;
    if (backTopBtn) backTopBtn.textContent = t.backTop;

    const audio = document.getElementById("ambientAudio");
    const musicStatus = document.getElementById("music-status");
    if (audio && musicStatus) {
        musicStatus.textContent = audio.paused ? t["music-status-disable"] : t["music-status-enable"];
    }
}

document.addEventListener("DOMContentLoaded", () => {
    const langSelector = document.getElementById("custom-content");
    changeLanguage(langSelector.value);

    langSelector.addEventListener("change", function () {
        changeLanguage(this.value);
    });

    const audio = document.getElementById("ambientAudio");
    const musicStatus = document.getElementById("music-status");

    if (musicStatus && audio) {
        musicStatus.style.cursor = "pointer";
        musicStatus.addEventListener("click", () => {
            if (audio.paused) audio.play();
            else audio.pause();
            changeLanguage(langSelector.value);
        });
        audio.addEventListener("play", () => changeLanguage(langSelector.value));
        audio.addEventListener("pause", () => changeLanguage(langSelector.value));
    }
});
